/*
 * 5. Escape Sequences in C++
 */

#include <iostream>
using namespace std;

int main() {
    // Newline
    cout << "Line 1\nLine 2";
    
    // Tab
    cout << "\n\nName\tAge";
    
    // Backslash
    cout << "\n\nBackslash: \\";
    
    // Quotes
    cout << "\n\n\"Quoted text\"";
    
    return 0;
} 
/*
 * 4. Output using cout in C++
 */

#include <iostream>
using namespace std;

int main() {
    // Basic output
    cout << "Hello, World!";
    
    // Variable output
    int age = 25;
    cout << "\nAge: " << age;
    
    // Calculation output
    cout << "\n5 + 3 = " << 5 + 3;
    
    return 0;
} 
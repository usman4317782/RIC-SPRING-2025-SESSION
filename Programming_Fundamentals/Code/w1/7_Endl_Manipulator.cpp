/*
 * 7. endl Manipulator in C++
 */

#include <iostream>
using namespace std;

int main() {
    // Using endl to create new lines
    cout << "Line 1" << endl;
    cout << "Line 2" << endl;
    cout << "Line 3" << endl;
    
    // Comparison with \n
    cout << "\nUsing \\n: Line 1\nLine 2\nLine 3";
    
    return 0;
} 